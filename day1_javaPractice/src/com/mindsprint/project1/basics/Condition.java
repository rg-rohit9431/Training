package com.mindsprint.project1.basics;

public class Condition {
    public static void main(String[] args) {
        int num = 10;
        int num2 = 20;

        System.out.println("num > num2: " + (num > num2));
        System.out.println("num < num2: " + (num < num2));
        System.out.println("num >= num2: " + (num >= num2));
        System.out.println("num <= num2: " + (num <= num2));
        System.out.println("num == num2: " + (num == num2));
        System.out.println("num != num2: " + (num != num2));
    }
}
